// Parallax

document.addEventListener("mousemove", parallax);
function parallax(e) {
  this.querySelectorAll('#parallax').forEach(phone => {
    const speed = phone.getAttribute('data-speed');
    
    const x = (window.innerWidth - e.pageX * speed)/100;
    const y = (window.innerHeight - e.pageY * speed)/100;
    
    phone.style.transform = `translateX(${x}px) translateY(${y}px)`;
  })
}



